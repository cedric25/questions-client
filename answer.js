'use strict';

module.exports = function(q) {

  if (q === 'Combien font 2 plus 2 ?') {
    return 4;
  }
  else if (q === 'Quelle est la capitale de la gastronomie Française ?') {
    return 'Lyon';
  }
  else if (q === 'Quel est le nombre en position 5 de la suite de Fibonacci (commençant par 0) ?') {
    return 5;
  }

};
